export const Icon: React.FC<IconProps> = ({
  name,
  size = 'md',
  color = 'current',
  className,
  children,
  onClick,
}) => {
  const iconContent = name ? iconSet[name] : children;

  if (!iconContent) {
    console.warn(`Icon "${name}" not found in iconSet`);
    return null;
  }

  const iconElement = React.cloneElement(iconContent as React.ReactElement, {
    className: cn(
      iconSizes[size],
      iconColors[color],
      onClick && 'cursor-pointer hover:opacity-75 transition-opacity',
      className
    ),
    onClick,
  });

  return iconElement;
};

// Helper components for specific use cases
export const IconButton: React.FC<{
  icon: keyof typeof iconSet;
  onClick: () => void;
  size?: IconProps['size'];
  variant?: 'ghost' | 'outline' | 'solid';
  className?: string;
  'aria-label': string;
}> = ({
  icon,
  onClick,
  size = 'md',
  variant = 'ghost',
  className,
  ...props
}) => {
  const variantStyles = {
    ghost: 'hover:bg-gray-100 dark:hover:bg-gray-800',
    outline:
      'border border-gray-300 hover:bg-gray-50 dark:border-gray-600 dark:hover:bg-gray-800',
    solid:
      'bg-gray-100 hover:bg-gray-200 dark:bg-gray-700 dark:hover:bg-gray-600',
  };

  return (
    <button
      onClick={onClick}
      className={cn(
        'inline-flex items-center justify-center rounded-lg p-2 transition-colors focus:outline-none focus:ring-2 focus:ring-blue-500',
        variantStyles[variant],
        className
      )}
      {...props}
    >
      <Icon name={icon} size={size} />
    </button>
  );
};
